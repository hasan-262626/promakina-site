import type { IntlPageContent } from "../types";

function buildPlantPage(
  breadcrumbLabel: string,
  title: string,
  description: string,
  intro: string,
  image: IntlPageContent["hero"]["image"],
  cards: { title: string; text: string }[],
  bullets: string[],
  rows: [string, string][],
  related: IntlPageContent["related"],
): IntlPageContent {
  return {
    meta: { title, description },
    breadcrumbLabel,
    hero: { h1: breadcrumbLabel, intro, image },
    sections: [
      { type: "cards", heading: "Состав решения", items: cards },
      { type: "bullets", heading: "Ключевые вопросы проектирования", items: bullets },
      {
        type: "table",
        heading: "Что нужно для предложения",
        columns: ["Входные данные", "Влияние на проект"],
        rows,
        note: "Окончательная конфигурация линии подтверждается по реальным анализам сырья и режиму работы.",
      },
    ],
    faq: [
      {
        q: "Можно ли реализовать проект поэтапно?",
        a: "Да. Мы можем разделить поставку на этапы: прием и подготовка, основной процесс и участок готового продукта.",
      },
      {
        q: "Входит ли в объем компоновка и автоматизация?",
        a: "Да. По запросу мы включаем layout, перечень оборудования, концепцию автоматизации и план пусконаладки.",
      },
    ],
    related,
    schemaType: "Service",
  };
}

